import type { PromptSummary, TimeSlot } from '$lib/domain/types';

function slot(id: string, promptId: string, date: string, hour: number, minute: number, area: string, lat: number, lng: number): TimeSlot {
	const d = new Date(`${date}T${String(hour).padStart(2,'0')}:${String(minute).padStart(2,'0')}:00`);
	return {
		id,
		prompt_id: promptId,
		start_time: d.toISOString(),
		duration_minutes: 90,
		general_area: area,
		general_area_lat: lat,
		general_area_lng: lng,
		accepted: false,
		created_at: '2026-06-01T10:00:00Z',
	};
}

