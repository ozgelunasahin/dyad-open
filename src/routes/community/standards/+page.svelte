	<h1>Community Standards</h1>
	<p class="subtitle">How we agree to be with one another.</p>

	<section>
		<p class="lede">The premise of dyad is that you are agreeing to meet a real person in a physical place. Everything here flows from that. The person across the table trusted this platform enough to show up. They are owed honesty, good faith, and the basic respect that makes a real encounter possible - not warmth, not agreement, not a good time. Just the baseline.</p>
	</section>

	<section>
		<h2>Community-authored, not platform-imposed</h2>
		<p>These standards are not rules handed down by a platform to a user base. They are agreements a community makes with itself, maintained and revised by the People's Assembly - the body of active members who govern community affairs. The distinction matters: community-authored standards have legitimacy that platform-imposed rules do not. If you disagree with a standard, the Assembly is the right place to bring that. You have a vote.</p>
		<p>Some standards are floors that cannot be lowered regardless of what the Assembly votes. These are marked below. They exist because some protections - against harassment, against exploitation - are not negotiable compromises between the community's convenience and an individual's safety. The Assembly can strengthen them. It cannot weaken them.</p>
	</section>

	<section>
		<h2>What we ask of everyone</h2>

		<h3>Show up</h3>
		<p>If you agree to a meeting, show up. This is the central commitment of membership. The platform is built on the premise that people can be trusted to follow through - that a stranger who agreed to meet you at a specific place and time will actually be there.</p>
		<p>Cancellations are allowed. Life intervenes. But cancellations carry weight, and they are visible to others who consider meeting you. Cancellations with more than 12 hours' notice are the least consequential. Cancellations with less than 12 hours' notice have more weight. No-shows - agreeing to meet and not appearing without any notice - are treated as a serious breach and are prominently reflected in your reputation.</p>
		<p>If something genuinely prevents you from attending and you cannot reach the platform in time to cancel, explain this in the post-meeting feedback form. Context is heard. Patterns of convenient last-minute emergencies are not.</p>

		<h3>Be honest</h3>
		<p>Be who you say you are. Not your most polished version - yourself. Do not misrepresent your background, your intentions, or your situation in ways that affect another person's decision to meet you. This is not about curating an appealing profile. It is about showing up as a real person to meet another real person.</p>

		<h3>Engage in good faith</h3>
		<p>You do not have to like the other person. You do not have to agree with them. The conversation does not have to be comfortable or easy. But it has to be a genuine attempt at one - not a performance, not an extraction, not a test. Both people showed up. That deserves a real effort.</p>

		<h3>Complete the feedback form</h3>
		<p>After every meeting, you complete the feedback form before accessing the platform again. This is a hard gate, not a recommendation. The form is how the community maintains accountability over time - how patterns of behaviour become visible, how trust accumulates, and how problems are caught before they repeat. You cannot skip it. There is no bypass.</p>
		<p>The feedback system is anonymous by default - your specific feedback is not attributed to you in the other person's profile. What is shared is the aggregate and certain behavioural signals. Use the system honestly. Gaming it - giving deliberately misleading feedback to punish someone for a legitimate interaction, or inflating your own reputation - is a violation.</p>

		<h3>One account</h3>
		<p>One person, one account. Creating additional accounts for any reason - to escape a reputation, to avoid a suspension, to appear as a different person, or simply to start fresh - is grounds for permanent removal. If you need to resolve a reputation issue, the right path is through the platform's appeals process, not a new account.</p>
	</section>

	<section>
		<h2>What is not allowed</h2>

		<h3>Harassment - floor protection</h3>
		<p>Any behaviour intended to intimidate, threaten, or cause distress to another member - before, during, or after a meeting - results in immediate removal from the platform. No warning. No probationary period. This includes messages sent after a meeting through any channel where the recipient has made clear they do not want contact.</p>
		<p>This standard is a floor. The People's Assembly cannot vote to relax it.</p>

		<h3>Sexual exploitation and coercion - floor protection</h3>
		<p>Using the platform to proposition, coerce, or exploit other members sexually results in immediate permanent removal and, where appropriate, will be reported to relevant authorities. This applies to behaviour during meetings, to patterns of intent visible in conversation prompts, and to behaviour that comes to light after the fact through reports.</p>
		<p>This standard is a floor. The People's Assembly cannot vote to relax it.</p>

		<h3>Commercial exploitation</h3>
		<p>Dyad is not a networking platform, a sales channel, or a recruitment tool. Using it to find clients, sell products or services, recruit for positions or causes, or otherwise extract commercial value from other members is a violation. This applies both to the conversation prompts you write and to how you conduct yourself in meetings.</p>

		<h3>Deception that affects safety</h3>
		<p>Misrepresenting your identity, your situation, or your intentions in ways that affect another person's safety or their decision to meet you is a serious violation. This includes representing yourself as a different gender, age, or professional context than you are, when the other person would have made a different decision had they known the truth.</p>

		<h3>Feedback system abuse</h3>
		<p>The feedback system exists to help the community maintain accountability. Using it to punish someone for a meeting that simply did not go well - where no standards were violated - undermines the system for everyone. Reviewers look for feedback that is disproportionately negative relative to the reported meeting experience. Patterns of this kind are treated as a standards violation.</p>

		<h3>Ban evasion</h3>
		<p>Returning to the platform after a ban under a different identity or email address is grounds for permanent removal upon discovery. If you believe your ban was unjust, the appeals process is the right mechanism. Creating a new account is not.</p>
	</section>

	<section>
		<h2>What happens when something goes wrong</h2>
		<p>If you experience a problem - a no-show, inappropriate behaviour during a meeting, harassment before or after - you report it through the platform using the feedback form or the separate incident report tool. Reports are reviewed by a community reviewer within 24 hours.</p>
		<p>For non-urgent concerns, you can also write to <a href="mailto:hello@dyad.berlin">hello@dyad.berlin</a>. For anything involving immediate safety concerns, contact the relevant emergency services first.</p>

		<h3>Outcomes by severity</h3>

		<div class="outcome-block">
			<div class="outcome">
				<div class="outcome-label">First instance, less severe violations</div>
				<p>A written warning explaining which standard was violated and why the behaviour fell below it. You are required to acknowledge the warning in writing before regaining full access. The warning is noted in your moderation record, visible to you but not to other members.</p>
			</div>

			<div class="outcome">
				<div class="outcome-label">Repeated violations or severe first instances</div>
				<p>A temporary suspension - duration depending on the nature of the violation, typically between 7 and 30 days. During suspension you cannot access the platform. A second violation of the same kind after returning from suspension escalates to a permanent ban.</p>
			</div>

			<div class="outcome">
				<div class="outcome-label">Harassment, sexual exploitation, ban evasion, or deception for safety reasons</div>
				<p>Immediate permanent removal. No warning. No suspension period. Where appropriate and legally required, the incident is reported to relevant authorities. Any remaining subscription period is not refunded.</p>
			</div>
		</div>
	</section>

	<section>
		<h2>Your right to respond</h2>
		<p>If you receive a warning or suspension that you believe is unjust, you have the right to submit a written response within 72 hours of receiving the decision. A different community reviewer - not the person who made the original decision - will review both the report and your response.</p>
		<p>We cannot share the specific reports or feedback of other members with you, for their privacy. But you will always know which standard you are alleged to have violated, and what specific behaviour triggered the review. You will have enough information to respond meaningfully.</p>
		<p>The reviewing member's decision is final at the reviewer level. If you believe the outcome reflects a systematic problem - a pattern of unfair enforcement, a biased reviewer, a standard that is itself unjust - you can bring this to the People's Assembly. The Assembly does not retry individual cases, but it does monitor patterns and can mandate reviews of enforcement practices.</p>
	</section>

	<section>
		<h2>How these standards evolve</h2>
		<p>The community standards are living documents. They are maintained by the People's Assembly through proposals, deliberation periods, and majority votes. Any member can propose a change. Changes require a supermajority and a minimum 14-day deliberation period before voting.</p>
		<p>The standards can be made stricter. The floor protections cannot be removed or weakened. The People's Assembly can expand them - adding additional floor protections - but cannot reduce them.</p>
		<p>When standards change, all members are notified and given time to review the changes before they take effect. If the changes affect rights or obligations in a material way, the process described in the <a href="/community/membership">membership agreement</a> applies.</p>
	</section>

