	<h1>Membership Agreement</h1>
	<p class="subtitle">What it means to be a member of dyad, and what we owe each other.</p>

	<section>
		<p class="lede">Membership is not a purchase. It is an entry into a mutual relationship - one that carries rights, responsibilities, and a real say in how this community governs itself. We are not selling access to a feed. We are building infrastructure for real connection, and the people who use it are its co-owners in practice, and eventually in legal structure.</p>
	</section>

	<section>
		<h2>What membership is</h2>
		<p>Being a member of dyad means full access to the platform: writing conversation prompts, responding to others', sending and receiving meeting invitations, and participating in the feedback cycle that follows every meeting.</p>
		<p>It also means being part of a community that governs itself. Active members - those who have completed at least five meetings - have the right to participate in the People's Assembly and vote on community governance decisions. Governance rights are earned through participation, not purchased through subscription. This is intentional: the people with the most experience of the platform should have the most say in how it is run.</p>
		<p>Membership requires a subscription - not to access a community you have to earn your way into, but to pay for the infrastructure that makes the community possible. The subscription is what makes the platform sustainable without advertising, without data sales, and without investor pressure to extract value from what you do here.</p>
	</section>

	<section>
		<h2>Pricing</h2>
		<p>There are three pricing tiers. All three give identical access - the same features, the same conversations, the same standing in the community. The difference is your relationship to the commons.</p>

		<div class="pricing-block">
			<div class="pricing-tier">
				<div class="tier-name">Community rate</div>
				<div class="tier-price">3–5 EUR / month</div>
				<p class="tier-desc">For members who need it. No justification required, no application, no questions asked. Choose this tier if it is what works for you. The community is designed to hold you.</p>
			</div>

			<div class="pricing-tier">
				<div class="tier-name">Sustaining rate</div>
				<div class="tier-price">7–9 EUR / month</div>
				<p class="tier-desc">What it actually costs, per member, to run dyad. If you pay this tier, your membership covers itself. Nothing more, nothing less. This is the honest number.</p>
			</div>

			<div class="pricing-tier">
				<div class="tier-name">Solidarity rate</div>
				<div class="tier-price">12–15 EUR / month</div>
				<p class="tier-desc">If you pay the solidarity rate, your contribution makes it possible for someone who cannot afford the sustaining rate to pay the community rate. The math is simple and we publish it. Your generosity is structural, not symbolic.</p>
			</div>
		</div>

		<p>We do not ask you to justify which tier you choose. We trust you. We publish the financials so you can see exactly what the numbers mean and where the money goes. Generosity is not the point - systems change is. The solidarity pricing model works because the math is transparent and the three tiers are identical in access. There is nothing to signal and nothing to prove.</p>
		<p>An annual payment option is available at a meaningful discount. This helps us with financial planning and you with cost.</p>
	</section>

	<section>
		<h2>What you get as a member</h2>
		<p>Access to the platform for as long as your subscription is active. The ability to write, respond, invite, and meet. The ability to build a history of meetings and a reputation that others can see when considering whether to meet you.</p>
		<p>For active members - defined as members who have completed at least five meetings - the right to participate in the People's Assembly and vote on community governance. The Assembly is how the community shapes its own standards, criteria, and policies. Your vote counts.</p>
		<p>The ability to participate in open conversations posted by founders or ambassadors, even before you have built up a meeting history. Open conversations are how the community seeds itself and how new members find their footing.</p>
		<p>Transparent financials, published annually. You are a stakeholder in this platform. You should know what it costs to run and where the money goes.</p>
	</section>

	<section>
		<h2>What you commit to</h2>

		<h3>The community standards</h3>
		<p>By becoming a member, you agree to the <a href="/community/standards">community standards</a>. These are not fine print. They are the terms on which real encounters become possible. Please read them.</p>

		<h3>Showing up</h3>
		<p>If you agree to meet someone, show up. This is the core commitment of membership. Cancellations are allowed - life happens - but they are tracked. No-shows without notice are taken seriously and affect the reputation others see when they consider meeting you.</p>

		<h3>Completing the feedback form</h3>
		<p>After every meeting, you complete a feedback form before you can access the platform again. This is not optional. The feedback gate is a hard requirement, not a nudge. It is how the community holds itself accountable and how trust accumulates over time. There is no grace period and no bypass.</p>

		<h3>Your identity</h3>
		<p>You commit to a consistent, genuine presence on the platform. You do not need to use your legal name - a pseudonym is fine, and your privacy is respected. But creating multiple accounts to escape a reputation, circumvent a suspension, or game the system is a serious violation and grounds for permanent removal. Your account is you. There is one of you.</p>
	</section>

	<section>
		<h2>Your data</h2>
		<p>We collect the minimum necessary to run the platform. What you write in conversation prompts. Meeting times and approximate locations. Feedback you give and receive. Your email address. The fact of your membership and its status.</p>
		<p>We do not sell your data. We do not use it for advertising - advertising is not a mechanism that exists in our business model. We do not share it with third parties for commercial purposes. The full, specific account of what we collect, why, and for how long is in the <a href="/datenschutz">privacy policy</a>.</p>
		<p>Your data is yours. You can request a full export at any time. You can delete your account at any time. Deleting your account removes your personal data from the system; some anonymised signals associated with meetings you participated in may remain to preserve the integrity of the other person's reputation history - your name and identity are not attached to them.</p>
	</section>

	<section>
		<h2>How membership ends</h2>

		<h3>If you choose to leave</h3>
		<p>You can cancel your subscription at any time. Your membership remains active until the end of the billing period you have already paid for. There is no penalty, no exit fee, no dark-pattern confirmation loop. You leave when you want to leave.</p>
		<p>If you delete your account, the other party in any pending meetings is notified and released from their obligations. Outstanding feedback forms are cancelled. Your reputation history is removed from your profile. Your personal data is deleted in accordance with the privacy policy.</p>

		<h3>If dyad ends your membership</h3>
		<p>Dyad can end your membership for violations of the community standards. The process for this is described in the <a href="/community/standards">community standards document</a>. In most cases, it begins with a warning and an opportunity to respond. For serious violations - harassment, sexual exploitation, ban evasion - removal is immediate and without warning.</p>
		<p>If your membership is ended involuntarily, you will receive a clear explanation of which standard was violated. You have the right to request a review within 72 hours.</p>
		<p>If you are removed for serious violations, any remaining subscription period is not refunded. If you are removed after a warning for less severe violations, we will consider a partial refund on a case-by-case basis.</p>
	</section>

	<section>
		<h2>Changes to this agreement</h2>
		<p>We will notify you by email in advance of any material changes to this agreement. Material changes are those that affect your rights or obligations as a member.</p>
		<p>If changes reduce your rights, you will have the option to cancel without penalty before they take effect. We will give you a minimum of 30 days' notice for any such change.</p>
		<p>Amendments to this agreement go through the People's Assembly. You have a vote. The process is described in the <a href="/community/governance">governance document</a>.</p>
	</section>

	<section class="doc-meta">
		<p>This agreement was adopted in May 2026. Current version: 1.0.</p>
		<p>Questions? Write to us at <a href="mailto:hello@dyad.berlin">hello@dyad.berlin</a>.</p>
	</section>
