	<h1>Acceptance Criteria</h1>
	<p class="subtitle">Dyad is curated, not closed. Here is what we look for, and why.</p>

	<section>
		<p class="lede">Curation is a structural decision, not a gatekeeping preference. The conditions that make real encounters possible - genuine intent, physical presence, honesty about who you are - cannot be maintained without some intentional shape to the community. We are not trying to be exclusive. We are trying to be what we say we are.</p>
		<p class="lede-secondary">The acceptance criteria are not set by us and then applied to you. They are set by the People's Assembly - the community governing itself - and applied by community reviewers who rotate. If you disagree with the criteria, you can change them through the Assembly once you are a member. That is the point of community governance.</p>
	</section>

	<section>
		<h2>The basic requirement: be in Berlin</h2>
		<p>Dyad is a hyper-local platform. The conversations happen in physical spaces. The maps shows you meeting locations. The slots have addresses. The whole point is that you show up somewhere in the city.</p>
		<p>If you are not in Berlin, or not coming to Berlin with any regularity, dyad is not for you yet. We will be honest about this upfront rather than let you join and discover it through frustration. When dyad opens in other cities, there will be a version for you there.</p>
		<p>We do not ask for proof of residency. Berlin is full of people with complicated residency situations - long-term visitors, people in transition, dual residents. What we ask for is honesty: are you genuinely able to show up for meetings in this city?</p>
	</section>

	<section>
		<h2>What we are looking for</h2>

		<h3>Genuine intent to meet</h3>
		<p>We are looking for people who actually want to have in-person conversations with strangers. Not to network in a professional sense. Not to build an audience. Not to find clients or recruits. To talk to someone, in a room, about something real.</p>
		<p>The platform is designed for this specific purpose. If your goal is something adjacent but different - finding collaborators, professional connections, romantic possibilities - there are better tools for those things. Dyad works best when everyone in it is there for the same reason.</p>

		<h3>A real and consistent presence</h3>
		<p>You do not need to use your legal name. Pseudonymity is respected and no proof of identity is required. But you need to be a real person with a consistent presence on the platform. We do not accept accounts created to test the system, throwaway accounts, or accounts created to circumvent a previous decision.</p>
		<p>A consistent presence means: one account, used by one person, showing up to the meetings they agree to, over time. Reputation is built through this consistency. The community depends on it.</p>

		<h3>An honest account of who you are</h3>
		<p>Your application asks you to tell us something about yourself and why you want to join. This is not a performance review. We are not looking for impressive credentials or polished self-presentation. We are looking for a genuine sense of who you are and what you are hoping to find here.</p>
		<p>A few sentences from a real person is more useful to us than a paragraph optimised to pass a filter. We are reading for authenticity, not quality.</p>
	</section>

	<section>
		<h2>How the review works</h2>
		<p>Applications are reviewed by active community members - not an algorithm, not a form validator. A real person reads your application, looks at what you have written, and makes a judgment call.</p>
		<p>What they are looking for: a sense that you will participate in good faith and show up to meetings you agree to. They are not judging your writing ability, your professional background, your politics, or your personality type. They are asking: does this person seem like they want to be here for the reason this platform exists?</p>
		<p>Reviews happen within 48 hours of your application. If we need more context, we will reach out by email before making a decision.</p>
		<p>Community reviewers rotate. No single person gatekeeps the community. Decisions are logged, and patterns - including patterns of reviewer bias - are monitored by the People's Assembly.</p>
	</section>

	<section>
		<h2>What disqualifies an application</h2>

		<h3>A prior ban</h3>
		<p>If you have previously been removed from dyad for a serious violation, a new application from the same person - under a different name or email - will be declined. We cross-reference email addresses and look for patterns of ban evasion. Attempting to circumvent a ban is itself a serious violation.</p>

		<h3>Clear commercial or exploitative intent</h3>
		<p>Applications where the intent is clearly commercial - finding clients, recruiting, promoting a service or product - are declined. Dyad is not a marketplace or a networking platform. Using it as one is a violation of the community standards regardless of how admission was obtained.</p>

		<h3>Evidence of intent to harm</h3>
		<p>Applications that give any evidence of intent to harass, exploit, or harm other members are declined immediately and flagged for review by the People's Assembly. Where appropriate, this is reported to relevant authorities.</p>

		<h3>Not in Berlin</h3>
		<p>If it is clear from your application that you are not in Berlin and have no genuine path to being here, the application is declined with an invitation to apply when your situation changes. This is the most common reason for a declined application, and the most easily resolved.</p>
	</section>

	<section>
		<h2>If your application is not accepted</h2>
		<p>You will receive a brief explanation of why. We do not leave people without a reason. In most cases the reason is timing - not in Berlin, or application did not give us enough to go on - not a judgment about who you are.</p>
		<p>You can apply again. A second application with more context, or after your situation changes, is always welcome. There is no cooling-off period and no strike against you for a declined application.</p>
		<p>If you believe the decision was wrong, you can request a review within seven days of receiving the decision. A different community reviewer - someone who was not involved in the original decision - will look at your application again. Their decision is final, but the case is logged and available for the People's Assembly to review if you believe the decision reflects a systematic problem rather than an individual error.</p>
	</section>

	<section>
		<h2>Open conversations as a path in</h2>
		<p>You do not have to wait for a successful application to experience what dyad is. Some conversations are open to non-members - posted by founders or by community members who have earned ambassador status through a history of good meetings.</p>
		<p>If you attend an open conversation, you experience the real thing. Not a demo. Not a preview. You book a slot, show up, have a meeting, and complete the feedback form. After completing an open conversation, you receive a personal invitation to apply for membership. The meeting itself is the pitch.</p>
	</section>

	<section>
		<h2>How these criteria change</h2>
		<p>The acceptance criteria are maintained by the People's Assembly - active members who participate in community governance. They are not set by us unilaterally, and they can change as the community evolves and as we learn what actually predicts good membership.</p>
		<p>If you are a member and believe the criteria are wrong - too strict, not strict enough, missing something important - that is a legitimate thing to bring to the Assembly. The criteria exist to serve the community, not to serve the platform's growth.</p>
	</section>

	<section class="doc-meta">
		<p>These criteria were adopted in May 2026. Current version: 1.0.</p>
		<p>Questions? Write to us at <a href="mailto:hello@dyad.berlin">hello@dyad.berlin</a>.</p>
	</section>
