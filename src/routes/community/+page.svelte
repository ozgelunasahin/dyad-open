	<h1>Our Charter</h1>
	<p class="subtitle">A manifesto, a set of commitments, and an invitation.</p>

	<section>
		<p class="lede">The platforms we use to meet, organise, and make sense of the world have become engines of extraction. Not by accident. By design. The March 2026 verdicts against Meta and YouTube did not reveal something new - they confirmed what years of research and lived experience had already made plain: platform design is intentional, and it is liable to its effects. Attention economies are attention economies on purpose.</p>
		<p class="lede-secondary">The typical response to this is ethical commitment. Companies publish values pages. Founders give interviews about doing things differently. Then they take equity investment, which obligates growth, which requires engagement optimisation, which means the values page was always a marketing document. Generosity and good intentions are no substitute for systems change. Bandcamp was beloved. Then it was sold, twice, in eighteen months. The community it served was not in the room when those decisions were made.</p>
		<p class="lede-secondary">Dyad exists because we believe the structural problem requires a structural answer. This charter is that answer stated plainly - not as a marketing document, but as a set of commitments we make to the people who join us, and as an account of why we have made them.</p>
	</section>

	<section>
		<h2>The problem we are responding to</h2>
		<p>Social platforms are built to maximise time-on-screen. This is not a side effect - it is the revenue model. Advertising pays for attention; platforms compete to capture it; the design of every feed, notification, and recommendation system reflects this competition. The result is platforms optimised to make you feel connected while systematically replacing the conditions for real connection with their simulacra.</p>
		<p>Author Cory Doctorow coined the term "enshittification" to describe how platforms decline: first they serve users, then they serve business customers, then they extract maximum value from both. Social platforms have followed this arc with particular speed. They cultivate communities, generate trust, and then monetise the trust by selling access to it. The community has no say. It was never in the ownership structure.</p>
		<p>The March 2026 verdicts confirm that this is not a neutral technical phenomenon - it is a choice, and it is one that societies are beginning to hold accountable. But legal accountability after the fact is not the same as structural accountability built in from the beginning. That is what we are trying to build.</p>
	</section>

	<section>
		<h2>What dyad is</h2>
		<p>A place to find someone to talk to, in person. You write a question or a subject you want to explore. Someone reads it and writes back. If both of you want to meet, you agree on a time and a place. Then you show up.</p>
		<p>That is the whole thing. The technology exists to make this as safe, trustworthy, and low-friction as possible - not to grow, not to engage, not to retain. Dyad gets out of the way once it has done its job of getting two people into the same room.</p>
		<p>The absence of feeds, algorithmic recommendations, social graphs, and engagement mechanics is not a missing feature set. It is a deliberate structural decision. We are building an enabler of real connection, not a replacement for it. Every feature that reduces friction by sorting you toward people like you accelerates the very isolation the platform exists to address. Efficiency is not the goal. Encounter is.</p>
	</section>

	<section>
		<h2>Something to say yes to</h2>
		<p>It is easy to be against things. What is harder - and more necessary - is proposing something worth saying yes to. Here is what dyad is for:</p>

		<div class="yes-list">
			<div class="yes-item">
				<div class="yes-label">Ownership by the people who use it</div>
				<p>Dyad is committed to transitioning to steward ownership - a legal structure in which voting control stays with the people who operate the platform, not external investors. Our pre-seed funding instrument is designed explicitly to make this transition possible. Community activation comes first. The legal structure follows.</p>
			</div>
			<div class="yes-item">
				<div class="yes-label">Aligned incentives, not managed conflicts</div>
				<p>Subscription funding means dyad succeeds when members value the platform enough to pay for it - not when they cannot stop scrolling. No advertising. No data sales. No engagement optimisation. The business model and the purpose point in the same direction.</p>
			</div>
			<div class="yes-item">
				<div class="yes-label">Governance by the community</div>
				<p>Active members govern this platform through the People's Assembly - a real decision-making body, not an advisory board. Community standards, acceptance criteria, and platform policy are set and changed by the members who live by them, not by us unilaterally.</p>
			</div>
			<div class="yes-item">
				<div class="yes-label">Transparency as practice, not performance</div>
				<p>We publish the financials. We explain the decisions. We tell you what it costs to run dyad and where the money goes. This charter is an example of what transparency looks like when it is a practice rather than a brand value.</p>
			</div>
			<div class="yes-item">
				<div class="yes-label">Connection as the endpoint, not the product</div>
				<p>Two people in a room having a real conversation is not a means to a platform metric. It is the thing itself. Dyad is designed around the quality of that encounter, not the quantity of time spent on screen before it.</p>
			</div>
		</div>
	</section>

	<section>
		<h2>Our real competition</h2>
		<p>Our competition is not another app. It is cynicism - the well-earned belief that structural change in how platforms are built and owned is not actually possible. That collective ownership doesn't scale. That mission statements always give way to investor returns. That the Bandcamp story will keep repeating.</p>
		<p>We understand this position. Cynicism is a rational response to repeated betrayal. But it is also an obstacle. When a platform built on genuine structural commitments - steward ownership, community governance, aligned incentives - demonstrates that it works, it does more than serve its members. It shows that the alternative is real.</p>
		<p>We are competing against:</p>
		<div class="competition-list">
			<div class="competition-item">The notion that platforms inevitably extract value from the communities they serve</div>
			<div class="competition-item">The assumption that investment requires surrendering governance to investors</div>
			<div class="competition-item">The belief that social connection cannot be supported by infrastructure that does not exploit it</div>
			<div class="competition-item">The comfort of the status quo, even when it is clearly not working</div>
		</div>
	</section>

	<section>
		<h2>What we commit to you</h2>

		<h3>Your data is yours</h3>
		<p>We collect only what is necessary to run the platform. We do not sell it, analyse it for advertising, or share it with third parties for commercial purposes. The full account of what we collect and why is in our <a href="/datenschutz">privacy policy</a> - written to be read, not to obscure.</p>

		<h3>No advertising, ever</h3>
		<p>Not now, not later, not under different leadership, not through a subsidiary. The revenue model has no mechanism for advertising and we are structuring the company so that no future leadership can introduce one. This will be encoded in the legal form of the company when we complete the steward ownership transition. Until then, it is a binding public commitment.</p>

		<h3>Radical transparency</h3>
		<p>We publish the financials. We explain our decisions. When we change course, we say so and explain why. Annual financial reports are available to all members. Governance decisions are communicated to the People's Assembly. Opacity is how platforms hide the gap between what they say and what they do. We are designing that gap out.</p>

		<h3>Real governance, not advisory theatre</h3>
		<p>The People's Assembly is not a focus group. It is the governing body for community standards, acceptance criteria, and platform policy. Active members vote. Decisions bind us. See the <a href="/community/governance">governance document</a> for how it works and what it can and cannot change.</p>

		<h3>A path to structural ownership</h3>
		<p>We have committed to steward ownership and structured our pre-seed funding to make the transition possible within eighteen months. The legal protections will follow the community, not precede it - because a governance structure worth protecting has to be proven real before it is locked in. We are proving it now.</p>
	</section>

	<section>
		<h2>What we ask of you</h2>

		<h3>Show up</h3>
		<p>If you agree to meet someone, be there. The platform functions on the premise that a stranger who agreed to meet you at a specific time and place will actually be there. That trust is the infrastructure. No-shows corrode it.</p>

		<h3>Be honest</h3>
		<p>Not polished. Honest. The person across the table is taking the same risk you are. They deserve the real version of you, not the optimised one.</p>

		<h3>Engage in good faith</h3>
		<p>The encounter does not have to be easy. It does not have to end in agreement. It has to be a genuine attempt - two people actually trying to be in conversation with each other, rather than performing it.</p>

		<h3>Participate in governance</h3>
		<p>Once you are active - five or more meetings completed - you have a vote in the People's Assembly. Use it. The community governs itself only if the community shows up for governance the same way it shows up for meetings.</p>
	</section>

	<section>
		<h2>About this document</h2>
		<p>This charter is a living document. It represents our current thinking, our current commitments, and our current understanding of what makes this work. It will change - through the People's Assembly, through experience, through learning what we got wrong.</p>
		<p>It is not a contract or a guarantee of specific future actions. It is an honest account of what we are trying to build and why, written to be held up against what we actually do. If there is a gap between this document and our behaviour, that gap is the accountability mechanism. Call it out.</p>
		<p>Some commitments are foundational and will be permanently locked when the steward ownership transition is complete - no advertising, no data sales, community governance preserved. Until then, they are binding on us as founders and on our investors through the terms of the funding instrument.</p>
		<p>The things that matter most should be the hardest to undo. That is the design.</p>
		<p>We should be honest about one further thing: we are doing this without a proven template. Community-governed, steward-owned platforms exist - but there are not many of them, and none have been through all the pressures that scale and time will bring. We have made the best structural choices we can with the knowledge available. Some of those choices will need revision. This document will change through the Assembly. What will not change - after the steward ownership transition - is the legal structure that makes the most important commitments irreversible.</p>
	</section>

