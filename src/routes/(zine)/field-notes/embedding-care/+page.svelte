			<p class="article-section">Introduction</p>
			<h1 class="article-title">Embedding care into the infrastructure</h1>
			<p class="article-byline">Luna Sahin, Co-founder & Steward · June 2026</p>
		</div>

		<div class="article-body">
			<p>Dyad began from a state of disillusionment that came through my own experience with harassment online, and with the strange powerlessness I mistook, at first, as personal.</p>

			<p>At the very moment I needed support, orientation, and care, the platforms I relied on revealed the distance they had built between themselves and the people who depend on them. I received no response to my support requests, no follow up to any of my numerous attempts for Meta to suspend the reported accounts. Stepping out from the rage and frustration with a peer group and research, I came to notice that what I had experienced as abandonment was not a glitch. It was a structural omission.</p>

			<p>What made this become the center of gravity for my work was watching the same dynamics scale far beyond my own experience. In a time of rising global tension, these systems were not helping us make sense of what was happening, or find our way through it. They were amplifying hostility, flattening complexity, rewarding outrage, and making it harder to tell what was true to act on. They were participating in the worsening of the very social fabric we would expect them to help hold.</p>

			<p>This brought us to a conviction. The individual, relational, and societal harms produced by incumbent social platforms are not accidental. They are part of how these systems are built to operate. When a platform is rewarded for attention capture, behavioral prediction, and extraction, it will continue to produce the conditions those incentives make likely. Care, trust, and connection cannot be expected from such a system, nor from surface-level interventions built on top of it.</p>

			<p>Staying with this long enough made the less visible questions impossible to ignore and the place we begin. Who owns the platforms we meet on? What incentives govern them? What is allowed remain hidden or unaccounted for? What is the company ultimately for? These questions widened our understanding of where innovation is most needed in social technology and infrastructure, and clarified what we were trying to protect and restore: the conditions under which people can find one another, meet uncertainty with more clarity grounded in community and solidarity, and act with greater agency.</p>

			<p>This is why we kept returning to conversations and community. For generations, conversation has been one of our primary tools for finding our way. We have used it to make sense of what is happening, test what we think we know, remember what matters, and decide what to do next. It is not a soft alternative to action. It is often where action becomes possible.</p>

			<p>Dyad begins from this old premise in a new condition. We began with an online software to help us find one another for conversations in real life, under a structure of collective ownership, participatory governance, and open infrastructure.</p>

			<p>The software is the first layer. In service to community and sensemaking, we plan to build an ecosystem consisting of a public conversation series, multimedia storytelling, and an editorial practice concerned rooted in epistemic justice. Our aim is to orchestrate the conditions under which better conversations can occur, and lead to wider participation, clearer sensemaking, and set example for collectively owned social technology.</p>

			<blockquote>The way these companies are built can feel like a law of nature. It is not. Ownership is a choice. Governance is a choice. Funding is a choice. Metrics are a choice. And they can be redesigned.</blockquote>

			<p>This document gathers the work of the past year and opens the path ahead. It holds our principles, our plans, our aspirations, and the long-term vision we are building toward. It is ambitious. We believe the moment asks for it.</p>

			<p>As you enter it, we invite you to set aside what you have come to expect from a social technology company. Collectively owned, transparent, and accountable social infrastructure is possible. Dyad is an attempt, and this, is our invitation to you.</p>
